export const getNarrationById = (id: string): Narration | undefined => {
  return narrations.find(narration => narration.id === id);
};

export const getNarrationByLesson = (lessonId: number): Narration[] => {
  return narrations.filter(narration => narration.id.includes(`lesson${lessonId}`));
};

export const getUbuntuPrincipleByLesson = (lessonId: number): UbuntuPrinciple | undefined => {
  return ubuntuPrinciples.find(principle => principle.lesson === lessonId);
}; 